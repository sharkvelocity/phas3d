// This module provides the definitive map manifest for the game.
// It directly sets the list of maps, ensuring consistency.

(function(){
  "use strict";
  window.PP = window.PP || {};
  
  // The corrected and complete list of maps available for investigation.
  const MAPS = [
      {
          id: 'procedural_house',
          title: 'Investigation Site',
          file: null // This map is generated by ProHouseGenerator.js, not loaded from a file
      }
  ];

  // Directly assign the manifest to ensure it's always correctly formatted.
  window.PP.mapManifest = MAPS;

  console.log("[map_loader] Map manifest updated to use procedural generator. Total maps:", window.PP.mapManifest.length);
  
  // Signal that this critical data is ready
  if (window.PP.signalReady) {
    window.PP.signalReady('mapLoader');
  }
})();